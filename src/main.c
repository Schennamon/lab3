u16 readADC1(u8 channel) {
	Usart_Log("Read ADC\n");
	ADC_RegularChannelConfig(ADC1, channel, 1, ADC_SampleTime_3Cycles);
	ADC_SoftwareStartConv(ADC1);
	while (ADC_GetFlagStatus(ADC1, ADC_FLAG_EOC) == RESET)
	{}
	return ADC_GetConversionValue(ADC1);
}

void check_cmd(uint8_t ch)
{
	Usart_Log("Check cmd\n");
	switch (ch) {
	case '1':
		mode_2_deinit();
		mode_1_init();
		state = MODE_1;
		break;
	case '2':
		mode_1_deinit();
		mode_2_init();
		state = MODE_2;
		break;
	default:
		break;
	}
}

void Usart_Log(uint8_t * buf)
{
	while(*buf != '\0') {
		USART_SendData(USART1, *buf);
        buf = buf + 1;
    }

}

