void TIM2_IRQHandler(void)
{
	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET) {
		TIM_ClearITPendingBit(TIM2, TIM_IT_Update);
		//анализ состояния кнопки
		if(GPIO_ReadInputDataBit(GPIOA, GPIO_Pin_0) == 0) {
			Usart_Log("Analizing button\n");
			switch (state) {
				case MODE_OFF:
					state = MODE_1;
					break;
				case MODE_1:
					mode_1_deinit();
					state = MODE_2;
					break;
				case MODE_2:
					mode_2_deinit();
					state = MODE_1;
					break;
				default:
					break;
			}
		}
		//мигание светодиодов
		switch (state) {
		    Usart_Log("Мигание светодиодов\n");
			case MODE_OFF:
				break;
			case MODE_1:
				GPIO_ToggleBits(GPIOD, GPIO_Pin_12);
				break;
			case MODE_2:
				GPIO_ToggleBits(GPIOD, GPIO_Pin_13);
				break;
			default:
				break;
		}

	}
}

void TIM6_IRQHandler(void)
{
	if (TIM_GetITStatus(TIM6, TIM_IT_Update) != RESET)
	{
		TIM_ClearITPendingBit(TIM6, TIM_IT_Update);
		if(MODE_1 == state)
		{
			//get value from ADC1 channel 1
		    Usart_Log("Get value from ADC1 channel 1\n");
			convert_code = readADC1(ADC_Channel_1);
		}
		else if(MODE_2 == state)
		{
			//get value from ADC1 channel 2
		    Usart_Log("Get value from ADC1 channel 2\n");
			convert_code = readADC1(ADC_Channel_2);
		}
	}
}

void USART1_IRQHandler(void){

	if( USART_GetITStatus(USART1, USART_IT_RXNE) )
	{
	    Usart_Log("GetITStatus\n");
		USART_ClearITPendingBit(USART1, USART_IT_RXNE);
		uint8_t t = USART_ReceiveData(USART1);
		check_cmd(t);

	}

}


